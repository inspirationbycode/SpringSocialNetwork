package com.dashaspivak.socialnetwork.logic;

public class Roles {
    private int id;
    private char name;

    public Roles() {
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(char name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public int getName() {
        return name;
    }
}
